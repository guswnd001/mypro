package hyeon.day_02;

public class 사칙연산 {
	public void 더하기(int 자료1, int 자료2){
		//더하기
		System.out.println("덧셈 결과  : " + (자료1 + 자료2));  
		return;  
	}
	public void 빼기(int 자료1, int 자료2){
		//빼기
		System.out.println("뺄셈 결과  : " + (자료1 - 자료2));
		return;
	}
	public void 곱하기(int 자료1, int 자료2){
		//곱하기
		System.out.println("곱셈 결과  : " + (자료1 * 자료2));
		return;
	}
	public void 나누기(int 자료1, int 자료2){
		//나누기
		System.out.println("나눗셈 결과  : " + (자료1 / 자료2));
		return;
	}
}
